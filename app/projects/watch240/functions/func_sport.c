u32 func_sport_get_disp_mode(void)//对应运动中显示运动数据种类->不同项目可自行添加->用于运动中与运动结束
{
    switch(sys_cb.sport_idx)
    {
        case SPORT_TYPE_RUNNING:        /*跑步 */
        case SPORT_TYPE_CLIMBING:       /*登山*/
        case SPORT_TYPE_WALKING:        /*健走|徒步 */
        case SPORT_TYPE_TREADMILL:      /*跑步机 */
#if UTE_MODULE_SPORT_HUNDRED_SUPPORT
        case SPORT_TYPE_INDOOR_WALK:    /*室内走路 */
        case SPORT_TYPE_STEP_TRAINING:  /*踏步 */
        case SPORT_TYPE_OUTDOOR_WALK:   /*户外健走 */
        case SPORT_TYPE_HIKING:         /*徒步 */
        case SPORT_TYPE_INDOOR_RUN:     /*室内跑步 */
        case SPORT_TYPE_PARKOUR:        /*跑酷 */
        case SPORT_TYPE_MARATHON:       /*马拉松 */
#endif
            return MULTIPLE_DATA;
        case SPORT_TYPE_JUMP_ROPE://跳绳
        case SPORT_TYPE_SWIMMING://游泳
            return MID_DATA;
        default:
            return LESS_DATA;
    }
}

